package inici;

public class OperacionesLogicas {
	public static void main(String[] args) {
		
		int a = 5;
		int b = 10;
		
		boolean resultado;
		
		resultado = a > b;
		System.out.println(resultado);
		
		resultado = a < b;
		System.out.println(resultado);
		
		resultado = a == b;
		System.out.println(resultado);
		
		resultado = a >= b;
		System.out.println(resultado);
		
		resultado = a <= b;
		System.out.println(resultado);
		
		resultado = a != b;
		System.out.println(resultado);
		
		boolean t = true;
		boolean f = false;
		
		resultado = t || f; // OR ||
		
		System.out.println(resultado);
		
		resultado = t && f; // AND $$
		
		System.out.println(resultado);
		
		boolean u = !f;
		System.out.println(u);
		
		
		
		
		
		
		
	}

}
